/**
 * Generate the grid.
 */
public class Grid {

	/**
	 * Lines number of the grid
	 */
	private int nbLine;
	/**
	 * Columns number of the grid.
	 */
	private int nbColumn;

	public int getNbColumn() {
		return this.nbColumn;
	}

	public int getNbLine() {
		return this.nbLine;
	}

}